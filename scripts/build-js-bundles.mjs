import fs from "node:fs/promises";
import path from "node:path";

const rootDir = process.cwd();
const manifestPath = path.join(rootDir, "scripts", "js-bundle-manifest.json");
const sourceRoot = path.join(rootDir, "src", "Subcontractor.Web", "wwwroot", "js");
const outputRoot = path.join(sourceRoot, "bundles");
const checkMode = process.argv.includes("--check");

function normalizeContent(value) {
    return value.replace(/\r\n/g, "\n").trimEnd() + "\n";
}

async function loadManifest() {
    const manifestRaw = await fs.readFile(manifestPath, "utf8");
    return JSON.parse(manifestRaw);
}

async function buildBundle(bundleName, sourceFiles) {
    if (!Array.isArray(sourceFiles) || sourceFiles.length === 0) {
        throw new Error(`Bundle "${bundleName}" has no source files.`);
    }

    const sections = [
        "/* Auto-generated by scripts/build-js-bundles.mjs. */",
        "/* Do not edit manually; update scripts/js-bundle-manifest.json and rebuild. */",
        ""
    ];

    for (const sourceFile of sourceFiles) {
        const sourcePath = path.join(sourceRoot, sourceFile);
        const sourceContent = await fs.readFile(sourcePath, "utf8");
        sections.push(`/* Source: ${sourceFile} */`);
        sections.push(normalizeContent(sourceContent));
    }

    return sections.join("\n");
}

async function main() {
    const manifest = await loadManifest();
    const bundles = manifest.bundles ?? [];
    if (!Array.isArray(bundles) || bundles.length === 0) {
        throw new Error("Bundle manifest is empty.");
    }

    await fs.mkdir(outputRoot, { recursive: true });

    let hasDrift = false;

    for (const bundle of bundles) {
        const bundleName = bundle.name;
        const sourceFiles = bundle.sources;
        const bundlePath = path.join(outputRoot, bundleName);
        const builtContent = await buildBundle(bundleName, sourceFiles);

        if (checkMode) {
            let existing = "";
            try {
                existing = await fs.readFile(bundlePath, "utf8");
            } catch {
                existing = "";
            }

            if (existing !== builtContent) {
                hasDrift = true;
                console.error(`Bundle drift detected: ${path.relative(rootDir, bundlePath)}`);
            }

            continue;
        }

        await fs.writeFile(bundlePath, builtContent, "utf8");
        console.log(`Built: ${path.relative(rootDir, bundlePath)}`);
    }

    if (checkMode && hasDrift) {
        process.exitCode = 1;
        return;
    }

    if (checkMode) {
        console.log("JS bundles are up to date.");
    }
}

await main();
